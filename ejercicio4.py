animales=['perro','gato','conejo','pez']
#Indexación positiva: Se usa para acceder a los elementos de la lista desde el principio índices 0, 1, 2,
animales[1] = 'loro'
#Indexación negativa: accede a los elementos desde el final índices -1 para el último, -2 para el penúltimo.
animales[-1] = 'oso'
print(animales)